export function generateMeditationText(surveyData, analysis, prompt) {
  return {
    script: buildMeditationScript(surveyData, analysis),
    promptMeta: {
      mode: "mock_generation",
      provider: "local-mock",
      promptPreview: prompt.userPrompt.slice(0, 240)
    }
  };
}

function buildMeditationScript(data, analysis) {
  const name = data.nickname || "你";

  const sceneTextMap = {
    森林: "一片安静的森林，空气里带着柔和的草木气息",
    海边: "一片开阔的海边，呼吸像海风一样慢慢舒展开",
    湖边: "一处平静的湖边，水面柔和，节奏也渐渐慢下来",
    山顶: "一处安静的高处，风轻轻掠过肩膀和胸口",
    房间: "一个安全、熟悉、只属于你的房间",
    城市夜景: "一片安静的城市夜景，灯光柔和地陪着你"
  };

  const bodyTextMap = {
    胃: "把注意力轻轻带到腹部，感受那里随着呼吸一下一下地起伏，像是在慢慢松开。",
    膝盖: "也许你会留意到双腿和膝盖，它们一直在默默支撑你，今天也可以稍微轻一点点。",
    腰背: "也许你会感受到腰背被好好托住，像有什么重量慢慢从那里放下来。",
    心肺: "把注意力带到胸口和呼吸，感受那里正在一点点变得更开阔。",
    睡眠: "让身体记住一种安稳的感觉，像夜晚终于可以更柔和地落下来。",
    整体精力: "把注意力带给整个身体，像是在为明天留下一点更稳定、更轻盈的能量。"
  };

  const foodMap = {
    奶茶: "你熟悉那种香甜、顺滑、很快就能带来满足感的味道。",
    甜食: "你熟悉那种甜甜的、软软的、很容易让情绪短暂停一下的感觉。",
    薯片: "你熟悉那种脆脆的、咸香的、会让手一片接一片伸过去的节奏。",
    油炸食品: "你熟悉那种浓郁、酥脆、很有犒劳感的味道。",
    夜宵: "你熟悉夜里那种终于可以松一下的安慰感。"
  };

  const voiceMap = {
    "今天太累了，应该奖励自己": "也许你会听见那个很累的自己。它不是想为难你，它只是很想被安慰。",
    "反正已经胖了，再吃一点也没关系": "也许你会听见那个想放弃一下的声音。你不需要责怪它，只需要知道，下一刻仍然可以重新选择。",
    "明天再开始": "也许你会听见那个想把一切推到明天的声音。而今天，你只需要先做一个很小的动作。",
    "我控制不住": "也许你会听见那个有点无力的声音。而你现在不需要一下控制全部，你只需要先停一下。",
    "不吃就浪费了": "也许你会听见那个舍不得的声音。而你的身体，也值得被温柔地照顾。",
    "没有明显声音": "也许你不需要马上找到一句明确的话，你只需要先听见身体的感觉。"
  };

  const replacementMap = {
    压力进食型: "下一次想靠吃来松一口气时，也许你可以先把注意力带回一口呼吸。",
    奖励型进食: "下一次想奖励自己时，也许你可以先问一句：还有没有一种方式，也会让身体觉得被好好照顾？",
    习惯型进食: "下一次熟悉的冲动出现时，也许你可以先停一下，看看这一次是不是还能做出一个更轻一点的选择。",
    自我控制困难型: "下一次你觉得自己又要滑回旧习惯时，也许你只需要先完成一个小动作，就已经是在重新站回自己这边。"
  };

  const pathFuture =
    analysis.primaryPath === "健康改善型"
      ? "你也许会慢慢看见，一个更轻松的自己正在出现：呼吸更开一些，走路时身体没那么沉，肩膀和腰背也更愿意放松下来。"
      : "你也许会慢慢看见，一个更轻盈的自己正在出现：肩膀更舒展，步伐更轻，穿衣时更自在，整个人的状态更柔和也更有光。";

  const animalToneMap = {
    鹿: "像鹿一样轻盈、敏锐而优雅",
    黑豹: "像黑豹一样稳定、有力量、动作流畅",
    天鹅: "像天鹅一样舒展、从容、轻轻掠过水面",
    猫: "像猫一样柔软、自在、知道什么时候该慢下来",
    海豚: "像海豚一样流动、轻快、呼吸顺畅"
  };

  return `${name}，现在，你可以先让身体找到一个更舒适一点的姿势。

也许你愿意，慢慢吸一口更深一点的气。
再轻轻地，把这一口气呼出去。

从头顶开始，到额头，到眼周，到下颌，再到肩膀、胸口、腹部和腰部，
你不需要做得很用力，只需要让身体一点点知道，现在是可以慢下来的一刻。

如果你愿意，你可以想象自己正来到${sceneTextMap[data.safeScene] || "一个安全、放松的地方"}。
在这里，不需要证明什么，也不需要立刻改变什么。

${bodyTextMap[data.bodyFocus] || "你可以把注意力轻轻带回身体，去感受身体正在发生什么。"}

也许你也会想到，你熟悉的那种食物。
${foodMap[data.favoriteFood] || "那是一种你很熟悉、也很容易让自己松一下的味道。"}
你不需要马上把它推开。
你只需要看见，为什么它对你来说像一种奖励。

当你把注意力从那一瞬间的满足，慢慢带回身体的时候，
也许你会发现，身体真正想要的，不只是那几秒钟的安慰，
它也在寻找一种更长久一点的轻松。

${voiceMap[data.innerVoice] || "你可以留意此刻心里出现的那句话。"}

这并不代表你做错了什么。
它只是说明，你一直在用你熟悉的方式照顾自己。

而现在，也许新的方式正在慢慢出现。
${analysis.psychDescription}

${replacementMap[analysis.psychProfile]}

${pathFuture}

如果你愿意，还可以把这种状态想象成一种感觉：
${animalToneMap[data.animalImage] || "更轻盈、更安静、更自在"}。

你的理想画面里，有${data.idealImage}。
而那不需要一下发生。
它可以从今天这一刻开始，先变成一个更柔和的方向。

当你准备好的时候，你可以再慢慢吸一口气。
让手指、脚趾轻轻动一动。
把这种被理解、被陪伴的感觉，带回今天剩下的时间里。`;
}
